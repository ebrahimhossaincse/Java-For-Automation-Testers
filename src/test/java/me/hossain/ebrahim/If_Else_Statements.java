package me.hossain.ebrahim;

public class If_Else_Statements {

	public static void main(String[] args) {
		// Defining a variable 'marks' to represent a student's score
		int marks = 75;

		// Using an if-else statement to check if the student has passed or failed
		if (marks >= 50) {                    // Condition: if 'marks' are 50 or above
		    System.out.println("Pass");        
		} else {
		    System.out.println("Fail");       
		}
	}

}
