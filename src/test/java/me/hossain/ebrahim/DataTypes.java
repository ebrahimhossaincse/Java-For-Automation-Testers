package me.hossain.ebrahim;

public class DataTypes {

	public static void main(String[] args) {
		// Demonstrating multiple data types in Java

		// Declaring an integer variable 'year' to store a whole number
		int year = 2024;

		// Declaring a double variable 'pi' to store a decimal value with precision
		double pi = 3.14159;

		// Declaring a boolean variable 'isJavaFun' to store a true or false value
		boolean isJavaFun = true;

		// Declaring a char variable 'firstLetter' to store a single character
		char firstLetter = 'E';
		
		// Declaring a String variable 'name' to store a value
		String name = "Ebrahim";

		// Printing each variable with a descriptive message to the console
		System.out.println("Year: " + year);              
		System.out.println("Pi value: " + pi);           
		System.out.println("Is Java fun? " + isJavaFun); 
		System.out.println("First letter: " + firstLetter);
		System.out.println("Name is: "+name);
	}

}
